"""Escreva um programa que leia um valor em metros e exiba convertido em centrimetros e milimetros"""

metro = int(input("Digite seu valor em metros:"))
centrimetros = metro*100
milimetro = metro*1000
print(f'O valor em metros é: {metro}\nEm centimetros é: {centrimetros}\nEm milimetros é: {milimetro}')
